def collect() -> None:
    fomc_saved     = _save_macro_events(_FOMC_EVENTS, "FOMC")
    cpi_saved      = _save_macro_events(_CPI_EVENTS,  "CPI")
    earnings_saved = _save_earnings_events(_EARNINGS_EVENTS)
    print(f"[calendar] FOMC {fomc_saved}건, CPI {cpi_saved}건, EARNINGS {earnings_saved}건 저장 완료")


def _save_macro_events(events: list[tuple], event_type: str) -> int:
    saved = 0
    with get_connection() as conn:
        for date_str, summary in events:
            with conn.cursor() as cur:
                cur.execute(
                    "SELECT 1 FROM market_event WHERE date = %s AND event_type = %s AND company_id IS NULL",
                    (date_str, event_type)
                )
                if cur.fetchone():
                    continue
                cur.execute(
                    "INSERT INTO market_event (company_id, date, event_type, summary) VALUES (NULL, %s, %s, %s)",
                    (date_str, event_type, summary)
                )
                saved += 1
    return saved


def _save_earnings_events(events: list[tuple]) -> int:
    saved = 0
    with get_connection() as conn:
        # ticker → company_id 매핑
        ticker_ids: dict[str, int] = {}
        with conn.cursor() as cur:
            cur.execute("SELECT ticker, id FROM company")
            for ticker, cid in cur.fetchall():
                ticker_ids[ticker] = cid

        for ticker, date_str, summary in events:
            company_id = ticker_ids.get(ticker)
            if company_id is None:
                print(f"  [skip] {ticker} — company 테이블에 없음")
                continue
            with conn.cursor() as cur:
                cur.execute(
                    "SELECT 1 FROM market_event WHERE date = %s AND event_type = %s AND company_id = %s",
                    (date_str, "EARNINGS", company_id)
                )
                if cur.fetchone():
                    continue
                cur.execute(
                    "INSERT INTO market_event (company_id, date, event_type, summary) VALUES (%s, %s, %s, %s)",
                    (company_id, date_str, "EARNINGS", summary)
                )
                saved += 1
    return saved
